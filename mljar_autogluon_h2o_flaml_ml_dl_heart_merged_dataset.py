aml = H2OAutoML(
    max_runtime_secs=180,
    nfolds=5,
    seed=42,
    sort_metric="f1",
    exclude_algos=None,
)

aml.train(x=x_cols, y=y_col, training_frame=train_h2o)

best_model = aml.leader

# 4) Prediction
preds = best_model.predict(test_h2o)  # columns: predict, p0, p1
preds_df = preds.as_data_frame()

