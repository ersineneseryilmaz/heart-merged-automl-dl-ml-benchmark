def get_system_ram():
    mem = psutil.virtual_memory()
    total = mem.total / (1024**3)
    used = mem.used / (1024**3)
    free = mem.available / (1024**3)
    percent = mem.percent
    return total, used, free, percent


def get_dir_size_mb(path: str) -> float:
    path = Path(path)
    if not path.exists():
        return np.nan
    if path.is_file():
        return path.stat().st_size / (1024 * 1024)
    total_bytes = 0
    for root, _, files in os.walk(path):
        for f in files:
            fp = Path(root) / f
            if fp.is_file():
                total_bytes += fp.stat().st_size
    return total_bytes / (1024 * 1024)


def pick_positive_proba_from_dataframe(proba_df: pd.DataFrame, pos_label=1) -> np.ndarray:
    """Return positive-class probabilities from AutoGluon-style DataFrame output."""
    if pos_label in proba_df.columns:
        return proba_df[pos_label].to_numpy()
    if str(pos_label) in proba_df.columns:
        return proba_df[str(pos_label)].to_numpy()
    return proba_df.iloc[:, -1].to_numpy()


def pick_positive_proba_from_array(proba) -> np.ndarray:
    """Return positive-class probabilities from sklearn/FLAML/H2O-like output."""
    proba = np.asarray(proba)
    if proba.ndim == 2 and proba.shape[1] >= 2:
        return proba[:, 1]
    if proba.ndim == 1:
        return proba
    return proba[:, -1]


def evaluate_binary(y_true, y_pred, y_proba=None) -> dict:
    """Compute binary classification metrics in the same style as the manuscript tables."""
    y_true = np.asarray(y_true).astype(int)
    y_pred = np.asarray(y_pred).astype(int)

    out = {
        "accuracy_pct": accuracy_score(y_true, y_pred) * 100,
        "f1_pct": f1_score(y_true, y_pred, zero_division=0) * 100,
        "precision_pct": precision_score(y_true, y_pred, zero_division=0) * 100,
        "recall_pct": recall_score(y_true, y_pred, zero_division=0) * 100,
    }

    if y_proba is not None:
        y_proba = np.asarray(y_proba, dtype=float)
        y_proba = np.clip(y_proba, 1e-7, 1 - 1e-7)
        out["auc_pct"] = roc_auc_score(y_true, y_proba) * 100
        out["logloss"] = log_loss(y_true, y_proba)
    else:
        out["auc_pct"] = np.nan
        out["logloss"] = np.nan

    tn, fp, fn, tp = confusion_matrix(y_true, y_pred, labels=[0, 1]).ravel()
    out.update({"tn": int(tn), "fp": int(fp), "fn": int(fn), "tp": int(tp)})
    return out


def monitor_start():
    process = psutil.Process(os.getpid())
    return {
        "process": process,
        "start_time": time.time(),
        "start_proc_mem": process.memory_info().rss / (1024 * 1024),
        "start_sys_used": get_system_ram()[1],
    }


def monitor_end(monitor: dict) -> dict:
    end_time = time.time()
    end_proc_mem = monitor["process"].memory_info().rss / (1024 * 1024)
    end_sys_used = get_system_ram()[1]
    return {
        "runtime_s": end_time - monitor["start_time"],
        "proc_ram_delta_mb": end_proc_mem - monitor["start_proc_mem"],
        "sys_ram_delta_gb": end_sys_used - monitor["start_sys_used"],
    }


