def safe_predict_proba(model, X_test):
    if hasattr(model, "predict_proba"):
        return model.predict_proba(X_test)
    return None


def compute_metrics(y_true, y_pred, y_proba, labels):
    """
    Computes binary or multiclass metrics.
    For multiclass tasks, macro-F1, weighted-F1, and balanced accuracy
    are especially important because the original 0–4 target is imbalanced.
    """
    n_classes = len(labels)

    result = {}

    result["accuracy"] = accuracy_score(y_true, y_pred)
    result["balanced_accuracy"] = balanced_accuracy_score(y_true, y_pred)

    if n_classes == 2:
        result["f1"] = f1_score(y_true, y_pred, average="binary", zero_division=0)
        result["f1_macro"] = f1_score(y_true, y_pred, average="macro", zero_division=0)
        result["f1_weighted"] = f1_score(y_true, y_pred, average="weighted", zero_division=0)
        result["precision_macro"] = precision_score(y_true, y_pred, average="macro", zero_division=0)
        result["recall_macro"] = recall_score(y_true, y_pred, average="macro", zero_division=0)

        if y_proba is not None:
            try:
                positive_proba = y_proba[:, 1]
                result["roc_auc"] = roc_auc_score(y_true, positive_proba)
                result["logloss"] = log_loss(y_true, y_proba, labels=labels)
            except Exception:
                result["roc_auc"] = np.nan
                result["logloss"] = np.nan
        else:
            result["roc_auc"] = np.nan
            result["logloss"] = np.nan

    else:
        result["f1"] = f1_score(y_true, y_pred, average="macro", zero_division=0)
        result["f1_macro"] = f1_score(y_true, y_pred, average="macro", zero_division=0)
        result["f1_weighted"] = f1_score(y_true, y_pred, average="weighted", zero_division=0)
        result["precision_macro"] = precision_score(y_true, y_pred, average="macro", zero_division=0)
        result["recall_macro"] = recall_score(y_true, y_pred, average="macro", zero_division=0)

        if y_proba is not None:
            try:
                result["roc_auc"] = roc_auc_score(
                    y_true,
                    y_proba,
                    labels=labels,
                    multi_class="ovr",
                    average="macro"
                )
                result["logloss"] = log_loss(y_true, y_proba, labels=labels)
            except Exception:
                result["roc_auc"] = np.nan
                result["logloss"] = np.nan
        else:
            result["roc_auc"] = np.nan
            result["logloss"] = np.nan

    return result


