def _holm_adjust(p_values):
    """Holm-Bonferroni adjusted p-values. Returns adjusted p-values in original order."""
    p_values = np.asarray(p_values, dtype=float)
    n = len(p_values)
    if n == 0:
        return []

    order = np.argsort(p_values)
    sorted_p = p_values[order]
    adjusted_sorted = np.empty(n, dtype=float)

    # Holm adjusted p for ordered p_i: max_{j<=i} ((m-j+1) * p_j), capped at 1.
    running_max = 0.0
    for rank, p in enumerate(sorted_p, start=1):
        adjusted = (n - rank + 1) * p
        running_max = max(running_max, adjusted)
        adjusted_sorted[rank - 1] = min(running_max, 1.0)

    adjusted = np.empty(n, dtype=float)
    adjusted[order] = adjusted_sorted
    return adjusted.tolist()


def _safe_friedman(pivot_df):
    """Run Friedman test on a complete paired pivot table: rows=seeds, cols=methods."""
    if pivot_df.shape[0] < MIN_PAIRED_RUNS or pivot_df.shape[1] < 3:
        return np.nan, np.nan, "skipped: requires at least 3 paired runs and 3 methods"

    arrays = [pivot_df[col].to_numpy(dtype=float) for col in pivot_df.columns]

    try:
        stat, p = friedmanchisquare(*arrays)
        return float(stat), float(p), "ok"
    except Exception as e:
        return np.nan, np.nan, f"skipped/error: {type(e).__name__}: {e}"


def _safe_wilcoxon(x, y):
    """Run paired Wilcoxon signed-rank test safely for small repeated-holdout samples."""
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    mask = np.isfinite(x) & np.isfinite(y)
    x = x[mask]
    y = y[mask]

    if len(x) < MIN_PAIRED_RUNS:
        return np.nan, np.nan, "skipped: fewer than minimum paired runs"

    diff = x - y
    if np.allclose(diff, 0):
        return 0.0, 1.0, "ok: all paired differences are zero"

    try:
        stat, p = wilcoxon(x, y, zero_method="wilcox", alternative="two-sided", mode="auto")
        return float(stat), float(p), "ok"
    except TypeError:
        # Older SciPy versions may not support mode="auto".
        try:
            stat, p = wilcoxon(x, y, zero_method="wilcox", alternative="two-sided")
            return float(stat), float(p), "ok"
        except Exception as e:
            return np.nan, np.nan, f"skipped/error: {type(e).__name__}: {e}"
    except Exception as e:
        return np.nan, np.nan, f"skipped/error: {type(e).__name__}: {e}"


