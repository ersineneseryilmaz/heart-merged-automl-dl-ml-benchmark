def build_sensitivity_models(n_classes, seed):
    """
    Lightweight classical ML baselines for target sensitivity analysis.
    These models are intentionally used as a robustness/sensitivity check,
    not as a replacement for the full AutoML benchmark.
    """
    models = {}

    models["LogisticRegression_balanced"] = Pipeline([
        ("scaler", StandardScaler()),
        ("clf", LogisticRegression(
            max_iter=2000,
            class_weight="balanced",
            random_state=seed,
            multi_class="auto"
        ))
    ])

    models["RandomForest_balanced"] = RandomForestClassifier(
        n_estimators=300,
        max_depth=None,
        min_samples_leaf=2,
        class_weight="balanced",
        random_state=seed,
        n_jobs=-1
    )

    models["ExtraTrees_balanced"] = ExtraTreesClassifier(
        n_estimators=300,
        max_depth=None,
        min_samples_leaf=2,
        class_weight="balanced",
        random_state=seed,
        n_jobs=-1
    )

    if HAS_XGBOOST:
        if n_classes == 2:
            models["XGBoost_fixed"] = XGBClassifier(
                n_estimators=300,
                max_depth=3,
                learning_rate=0.05,
                subsample=0.9,
                colsample_bytree=0.9,
                objective="binary:logistic",
                eval_metric="logloss",
                random_state=seed,
                n_jobs=-1
            )
        else:
            models["XGBoost_fixed"] = XGBClassifier(
                n_estimators=300,
                max_depth=3,
                learning_rate=0.05,
                subsample=0.9,
                colsample_bytree=0.9,
                objective="multi:softprob",
                eval_metric="mlogloss",
                num_class=n_classes,
                random_state=seed,
                n_jobs=-1
            )

    if HAS_LIGHTGBM:
        if n_classes == 2:
            models["LightGBM_fixed"] = LGBMClassifier(
                n_estimators=300,
                max_depth=-1,
                learning_rate=0.05,
                class_weight="balanced",
                random_state=seed,
                n_jobs=-1,
                verbose=-1
            )
        else:
            models["LightGBM_fixed"] = LGBMClassifier(
                n_estimators=300,
                max_depth=-1,
                learning_rate=0.05,
                objective="multiclass",
                class_weight="balanced",
                random_state=seed,
                n_jobs=-1,
                verbose=-1
            )

    return models


