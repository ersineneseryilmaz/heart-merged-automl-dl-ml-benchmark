    budget_summary["compliance_rate_pct"] = budget_summary["compliant_runs"] / budget_summary["n_runs"] * 100
    budget_summary.to_csv(BUDGET_CSV, index=False)

    print(f"Saved summary: {SUMMARY_CSV}")
    print(f"Saved formatted summary: {SUMMARY_FORMATTED_CSV}")
    print(f"Saved budget-compliance summary: {BUDGET_CSV}")

    return summary, formatted, budget_summary


