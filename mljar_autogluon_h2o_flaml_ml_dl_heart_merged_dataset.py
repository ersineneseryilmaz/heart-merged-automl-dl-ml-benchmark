def fmt_mean_sd(mean_val, std_val, percent=True):
    if pd.isna(mean_val):
        return "NA"

    if percent:
        mean_val = mean_val * 100
        std_val = std_val * 100

    if pd.isna(std_val):
        return f"{mean_val:.2f}"

    return f"{mean_val:.2f} ± {std_val:.2f}"


