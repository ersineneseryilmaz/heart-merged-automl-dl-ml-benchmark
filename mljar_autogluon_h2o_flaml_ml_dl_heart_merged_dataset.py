        runtime_mean_s=("runtime_s", "mean"),
        runtime_sd_s=("runtime_s", "std"),
        overhead_mean_s=("runtime_minus_budget_s", "mean"),
        max_overhead_s=("runtime_minus_budget_s", "max"),
        runtime_to_budget_ratio_mean=("runtime_to_budget_ratio", "mean"),
