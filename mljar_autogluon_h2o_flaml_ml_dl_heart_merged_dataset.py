def run_classic_ml_light_once(data: pd.DataFrame, seed: int) -> dict:
    """Fast classical ML pool with inner validation selection and held-out test evaluation."""
    set_all_seeds(seed)
    rec = base_record("ClassicML_Light", seed)
    mon = monitor_start()

    X = data.drop(columns=[TARGET_COL])
    y = data[TARGET_COL].astype(int)

    X_train_outer, X_test, y_train_outer, y_test = train_test_split(
        X, y, test_size=TEST_SIZE, random_state=seed, stratify=y
    )
    X_train, X_val, y_train, y_val = train_test_split(
        X_train_outer, y_train_outer,
        test_size=INNER_VAL_SIZE,
        random_state=seed,
        stratify=y_train_outer
    )

    candidates = {
        "LogisticRegression": Pipeline([
            ("scaler", StandardScaler()),
            ("model", LogisticRegression(max_iter=1000, random_state=seed))
        ]),
        "RandomForest": RandomForestClassifier(
            n_estimators=200, random_state=seed, n_jobs=-1, class_weight=None
        ),
        "ExtraTrees": ExtraTreesClassifier(
            n_estimators=300, random_state=seed, n_jobs=-1, class_weight=None
        ),
    }

    try:
        from xgboost import XGBClassifier
        candidates["XGBoost"] = XGBClassifier(
            n_estimators=120,
            max_depth=3,
            learning_rate=0.08,
            subsample=0.9,
            colsample_bytree=0.9,
            eval_metric="logloss",
            random_state=seed,
            n_jobs=-1,
        )
    except Exception as e:
        print(f"[ClassicML] XGBoost skipped: {e}")

    try:
        from lightgbm import LGBMClassifier
        candidates["LightGBM"] = LGBMClassifier(
            n_estimators=120,
            learning_rate=0.05,
            random_state=seed,
            n_jobs=-1,
            verbose=-1,
        )
    except Exception as e:
        print(f"[ClassicML] LightGBM skipped: {e}")

    best_name, best_model, best_val_f1 = None, None, -np.inf
    for name, model in candidates.items():
        model.fit(X_train, y_train)
        val_pred = model.predict(X_val)
        val_f1 = f1_score(y_val, val_pred, zero_division=0)
        if val_f1 > best_val_f1:
            best_name, best_model, best_val_f1 = name, model, val_f1

    y_pred = best_model.predict(X_test)
    y_proba = None
    if hasattr(best_model, "predict_proba"):
        y_proba = pick_positive_proba_from_array(best_model.predict_proba(X_test))

    model_path = ARTIFACT_DIR / f"classic_ml_light_seed_{seed}_{best_name}.pkl"
    joblib.dump(best_model, model_path)

    rec.update(evaluate_binary(y_test, y_pred, y_proba))
    rec.update(monitor_end(mon))
    rec.update({
        "best_model": best_name,
        "inner_val_f1_pct": best_val_f1 * 100,
        "model_size_mb": get_dir_size_mb(str(model_path)),
    })
    return rec


