def _make_analysis_sets(df):
    """
    Build paired comparison sets.
    1) Framework-level selected/framework runs: compares AutoML frameworks or selected classical ML wrapper if available.
    2) Candidate-model comparison within each framework: compares model candidates across the same repeated splits.
    3) All method-level rows: compares every unique framework/model combination when paired seeds exist.
    """
    sets = []

    # A) Framework-level selected/framework runs
    selected_labels = {
        "selected_by_inner_validation",
        "framework_run",
        "automl_framework",
        "automl_run",
        "selected_model",
    }
    selected_df = df[df["selection_type"].astype(str).isin(selected_labels)].copy()
    if not selected_df.empty:
        selected_df["method"] = selected_df["framework"].astype(str)
        sets.append(("framework_level_selected_runs", selected_df))

    # B) Candidate-model comparisons within each framework
    candidate_df = df[df["selection_type"].astype(str).eq("candidate_model")].copy()
    if not candidate_df.empty:
        for fw, g in candidate_df.groupby("framework"):
            g = g.copy()
            g["method"] = g["model"].astype(str)
            sets.append((f"candidate_models_within_{fw}", g))

    # C) Generic method-level comparison for all rows
    generic_df = df.copy()
    generic_df["method"] = np.where(
        generic_df["model"].astype(str).eq(generic_df["framework"].astype(str)),
        generic_df["framework"].astype(str),
        generic_df["framework"].astype(str) + " | " + generic_df["model"].astype(str),
    )
    sets.append(("all_available_method_rows", generic_df))

    return sets


def _paired_pivot(g, metric):
    """Return complete paired pivot with rows=seed, columns=method."""
    if metric not in g.columns:
        return pd.DataFrame()

    # Average duplicate seed/method rows if any exist.
    pivot = (
        g.pivot_table(index="seed", columns="method", values=metric, aggfunc="mean")
        .sort_index()
    )

    # Keep only methods with at least MIN_PAIRED_RUNS observations.
    valid_cols = [c for c in pivot.columns if pivot[c].notna().sum() >= MIN_PAIRED_RUNS]
    pivot = pivot[valid_cols]

    # Friedman/Wilcoxon should use matched seeds; drop incomplete rows.
    pivot = pivot.dropna(axis=0, how="any")
    return pivot


def _better_method(metric, method_a, mean_a, method_b, mean_b):
    direction = METRIC_DIRECTION.get(metric, "higher")
    if np.isclose(mean_a, mean_b, equal_nan=False):
        return "tie"
    if direction == "lower":
        return method_a if mean_a < mean_b else method_b
    return method_a if mean_a > mean_b else method_b


